@Entity
@Table(name = "customers")
@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
public class Customer {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Column(name = "id")
    private Long id;

    @NotBlank
    private String name;

    @NotBlank
    private String email;

    private String phone;

    @OneToOne(mappedBy = "customer")
    private Account account;
}
